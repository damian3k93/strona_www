<h2>Krótka charakterystyka spływu szlakiem rzeki Sapiny</h2>
Spływ szlakiem Sapiny, to około 35 km spływu zalesionymi rzekami i jeziorami.
Spływ Sapiną najczęściej rozpoczynamy w miejscowości Kruklin i jeziorze Kruklin, a kończymy w Ogonkach lub po pokonaniu jeziora Mamry - w Węgorzewie.
Spływ Sapiną można rozłożyć na 2 etapy z zachowaniem relaksowego tempa.
Na miejsca noclegów można wybrać pola namiotowe lub (niestety niezbyt liczne) ośrodki i pensjonaty.
W sytuacji, kiedy nie mamy dokonanych rezerwacji noclegów, musimy zabrać ze sobą koniecznie namioty i korzystać z nich, gdyby nie udało się "z marszu" znaleźć wolnych miejsc noclegowych. 