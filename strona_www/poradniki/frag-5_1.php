<h1>Bezpieczeństwo</h1>
Spływ kajakowy jest bardzo przyjemną i wyjątkowo atrakcyjną formą aktywnego spędzania wolnego czasu.
Staje też się coraz bardziej popularny.
Wszystko to powoduje, że do kajaków wsiadają i próbują swych sił na spływie kajakowym osoby często zupełnie do tego nie przygotowane.
By choć w minimalnym stopniu przybliżyć zupełnie podstawowe zasady wpływające na zachowanie bezpieczeństwa na spływie kajakowym 
podajemy poniżej kilka takich zupełnie podstawowych zasad pływania kajakiem na rzekach i jeziorach.
Kilka podstawowych rad i porad opartych na praktyce i doświadczeniu innych poznane, przemyślane i później konsekwentnie stosowane
 na pewno poprawią bezpieczeństwo na spływie i sprawią by spływ kajakowy był równie piękny co bezpieczny.
