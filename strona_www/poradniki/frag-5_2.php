<h1>Co zabrać?</h1>
Na temat tego co trzeba koniecznie zabrać ze sobą na spływ kajakowy jest wiele teorii i "swoistych" list niezbędnych rzeczy.
Skład ich zależy też w dużej mierze od sposobu organizacji i zastosowanych rozwiązań (np. 
noclegów) na spływie lub choćby od wyboru szlaku kajakowego.
W praktyce każdy z kajakarzy w krótkim czasie wypracowuje swoją i jej się trzyma.
Oczywiście w zasadniczych rzeczach wszystkie "listy" są zbieżne , a różnią się tylko w szczegółach.
Możemy więc spróbować nakreślić rzeczy najbardziej podstawowe,
 a reszta przyjdzie z indywidualną praktyką każdego z uczestników spływów kajakowych.
