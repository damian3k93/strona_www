 decyduje organizator wykorzystując do maksimum posiadaną przez siebie bazę i jest to najzupełniej zrozumiałe.
Ma do tego prawo i jeśli przedstawiona oferta zadowala klienta, to wszystko jest oczywiście jak najbardziej w porządku.

