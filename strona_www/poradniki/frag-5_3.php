<h1>Organizacja...</h1>
Właściwie, to każdy może sobie sam zorganizować spływ kajakowy.
Wystarczy mieć kajak, transport, wybrać szlak, spakować trochę rzeczy i… HAJDA !..
Niestety, nie zawsze jednak jest to takie proste.
Tak mogą oczywiście mogą organizować spływy wszyscy "zaprawieni w bojach" kajakarze i fascynaci tej formy turystyki, ponieważ 
dysponują odpowiednim doświadczeniem organizatorskim i kajakarskim oraz potrzebnym sprzętem i możliwością jego transportu.
