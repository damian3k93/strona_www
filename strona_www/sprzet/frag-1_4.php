<h1>Kajaki czteroosobowe</h1>
Bardzo wygodny (wyposażony w wygodne siedzenia), odporny na uszkodzenia (polietylen) i bardzo stabilny kajak z możliwością zapakowania dużej ilości bagaży i oczywiście wyposażony w dodatkowe siedzenia dla dwojga małych (w wieku do ok. 3 lat) dzieci. Bardzo dobry, tak na długie trasy, pokonywanie jezior, jak i na kilkugodzinne, rodzinne spływy.
Wyposażenie: wiosła, kamizelki asekuracyjne, siedziska i ew. na życzenie (odpłatnie) wodoodporne beczki lub worki na bagaż<br><br>
<h3>Dostępne rozmiary:</h3>
•	 495 cm<br>
•	 87 cm<br>
•	 30 cm<br>
•	 320 km<br>
<br>
<b>Cena wynajmu 33 zł / dzień</b></br>
