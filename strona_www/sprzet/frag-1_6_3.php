Kamizelki stanowią część wyposażenia wynajętego sprzętu i jako taka wypożyczane są bezpłatnie. 
Kamizelki asekuracyjne służą zapewnieniu bezpieczeństwa uczestnikom spływów kajakowych. Aby mogły spełniać swoją rolę, 
powinny być założone. Kamizelki dostępne są w różnych rozmiarach ze szczególnym uwzględnieniem rozmiarów dziecięcych.
