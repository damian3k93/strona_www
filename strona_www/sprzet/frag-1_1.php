 <h1>Kajaki jednoosobowe</h1><br>
 Bardzo zwrotny, stabilny i szybki kajak na krótkie wyprawy z niewielkim bagażem podręcznym lub wyprawy dłuższe z opcją przewozu bagaży. Wyposażony w wygodne i składane siedzenie, siatki na bagaże na dziobie i rufie oraz rączki do noszenia.
Wyposażenie: wiosło, kamizelka asekuracyjna i na życzenie (odpłatnie) - fartuch, wodoszczelne worki lub beczki na bagaż
