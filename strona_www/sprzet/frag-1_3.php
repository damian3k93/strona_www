<h1>Kajaki trzyosobowe</h1>
Bardzo bezpieczny i wygodny. Idealny dla par z dzieckiem (do ok. 11-12 lat) lub dużą ilością bagaży. Używany tak na jednodniowych jak i wielodniowych spływach kajakowych.
Wyposażenie: wiosła, kamizelki asekuracyjne, siedziska i ew. na życzenie (odpłatnie) wodoodporne beczki lub worki na bagaż<br><br>
<h3>Dostępne rozmiary</h3>
•	 560 cm<br>
•	 80 cm<br>
•	 45 cm<br>
•	 420 kg<br>
<br>
