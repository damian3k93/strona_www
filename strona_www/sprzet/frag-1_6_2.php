Siedzisko stanowi część wyposażenia niektórych kajaków (takich, które nie są wyposażone w specjalne siedzenia). 
Do takich kajaków wydawane są bezpłatnie. Do kajaków z siedzeniami z polietylenu wydawane są na życzenie za odpłatnością 1 zł / 1 szt.
/ dzień. Siedziska wykonane są z gąbki lub poliestru, bardzo zwiększają komfort, tak na jednodniowym, jak i kilkudniowym spływie kajakowym.
 </div>