Wiosła wchodzą w skład wyposażenia kajaków i jako takie są wypożyczane bezpłatnie.
Wiosła dostępne w wypożyczalni są wykonane z drewna lub aluminium i plastiku lub polietylenu.
 Wszystkie są bardzo lekkie i wygodne. Używane są na jednodniowych i wielodniowych spływach kajakowych.
