<h1>Kajaki dwuosobowe</h1>
Bardzo wygodny (wyposażony w wygodne siedzenia), odporny na uszkodzenia ( polietylen ) i bardzo stabilny kajak z możliwością zapakowania dużej ilości bagaży. Bardzo dobry na długie trasy i pokonywanie jezior (doskonale "trzyma kurs").
Wyposażenie: wiosła, kamizelki asekuracyjne, siedziska i ew. na życzenie (odpłatnie) wodoodporne beczki lub worki na bagaż
